public void show_pets() {
        DefaultListModel pets_list = new DefaultListModel();
        int index_list = 0;
        for (DogPetClass dog : dogObjectList) {
            String registro_mascota = dog.getCode() + " - " + dog.getName() + " - " + dog.getBreed() + " - " + dog.getHealth_status() + " - " + dog.getBorn_yearn() + " - " + dog.getColor() + " - " + dog.isPedigree() + dog.getPet_sex();
            pets_list.add(index_list, registro_mascota);
            index_list++;
        }
        pet_list.setModel(pets_list);
    }
