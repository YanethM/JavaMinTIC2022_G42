    private void btn_pet_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pet_searchActionPerformed
        // TODO add your handling code here:
        int code = Integer.parseInt(txt_code_search.getText());
        boolean find_pet = false;
        for (DogPetClass dog : dogObjectList) {
            if (code == dog.getCode()) {
                lbl_result_search.setText("<html> Nombre mascota: " + dog.getName() + "<br/>Color: " + dog.getColor() + "</html>");
                find_pet = true;
                break;
            }

        }
        if (!find_pet) {
            JOptionPane.showMessageDialog(this, "No se encontro ninguna mascota con el código suministrado");
        }
    }//GEN-LAST:event_btn_pet_searchActionPerformed

